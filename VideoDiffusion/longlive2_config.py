"""Generate and validate LongLive2 inference configs for repo-run workflows."""

from __future__ import annotations

import argparse
import csv
import json
import math
import tempfile
from dataclasses import dataclass
from pathlib import Path


DEFAULT_REPO_REF = "536d1b9a3563b078bea378aa968416543fd9d669"
DEFAULT_MODEL_NAME = "Wan2.2-TI2V-5B"
DEFAULT_SCENE_CUT_PREFIX = "The scene transitions. "
DEFAULT_NEGATIVE_PROMPT = (
    "overexposed, static, blurry details, subtitles, low quality, jpeg artifacts, "
    "deformed hands, deformed face, malformed limbs, cluttered background"
)


@dataclass(frozen=True)
class ConfigSpec:
    profile: str
    height: int
    width: int
    frames: int
    sp_size: int
    dp_size: int
    prompt_path: Path
    output_folder: Path
    generator_ckpt: str
    lora_ckpt: str
    sampling_steps: int
    model_num_heads: int
    num_frame_per_block: int
    guidance_scale: float
    seed: int
    vae_device: str
    torch_compile: str


def valid_sp_sizes(model_num_heads: int, num_frame_per_block: int) -> list[int]:
    base = math.gcd(model_num_heads, num_frame_per_block)
    return [size for size in range(1, base + 1) if base % size == 0]


def _bool_text(value: bool) -> str:
    return "true" if value else "false"


def _yaml_scalar(value: str | int | float | bool) -> str:
    if isinstance(value, bool):
        return _bool_text(value)
    if isinstance(value, (int, float)):
        return str(value)
    escaped = str(value).replace("'", "''")
    return f"'{escaped}'"


def _normalize_profile(raw: str) -> str:
    profile = raw.strip().lower().replace("-", "_")
    if profile in {"bf16", "bf16_sp"}:
        return "bf16_sp"
    if profile in {"nvfp4", "nvfp4_s4", "nvfp4_4step", "nvfp4_s4_sp"}:
        return "nvfp4_s4"
    if profile in {"nvfp4_s2", "nvfp4_2step", "nvfp4_s2_sp"}:
        return "nvfp4_s2"
    raise ValueError(f"unsupported profile '{raw}'")


def _num_blocks(frames: int, num_frame_per_block: int) -> int:
    if frames <= 0 or num_frame_per_block <= 0:
        raise ValueError("frames and num_frame_per_block must be positive")
    if frames % num_frame_per_block != 0:
        raise ValueError(f"frames must be divisible by num_frame_per_block={num_frame_per_block}")
    return frames // num_frame_per_block


def validate_spec(spec: ConfigSpec) -> None:
    if spec.height <= 0 or spec.width <= 0:
        raise ValueError("height and width must be positive")
    if spec.height % 16 != 0 or spec.width % 16 != 0:
        raise ValueError("height and width must be divisible by 16")
    if spec.frames <= 0:
        raise ValueError("frames must be positive")
    if spec.frames % spec.num_frame_per_block != 0:
        raise ValueError(f"frames must be divisible by num_frame_per_block={spec.num_frame_per_block}")
    if spec.sp_size <= 0 or spec.dp_size <= 0:
        raise ValueError("sp_size and dp_size must be positive")
    allowed = valid_sp_sizes(spec.model_num_heads, spec.num_frame_per_block)
    if spec.sp_size not in allowed:
        raise ValueError(
            f"sp_size={spec.sp_size} must divide gcd(model_num_heads={spec.model_num_heads}, "
            f"num_frame_per_block={spec.num_frame_per_block}); valid={allowed}"
        )
    if not str(spec.prompt_path):
        raise ValueError("prompt_path is required")
    if not str(spec.output_folder):
        raise ValueError("output_folder is required")


